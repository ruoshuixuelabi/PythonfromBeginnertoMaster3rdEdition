import numpy as np                # 导入numpy模块

a = np.random.random(5)            # 创建随机数组
b = np.random.random()             # 创建无参数随机数组
c = np.random.random((2,3))        # 创建指定外形的随机数组
print('数组a内容为： \n',a)        # 打印数组a内容
print('数组b内容为： \n',b)        # 打印数组b内容
print('数组c内容为： \n',c)        # 打印数组c内容
